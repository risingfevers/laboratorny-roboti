import numpy as np

# Матрица коэффициентов
A = np.array([
    [9, -5, -6, 3],
    [1, -7, 1, 0],
    [3, -4, 9, 0],
    [6, -1, 9, 8]
])

# Вектор правых частей
b = np.array([-8, 38, 47, -8])

# Решение системы уравнений
x = np.linalg.solve(A, b)

print(f"Решение системы матричным методом: {x}")
